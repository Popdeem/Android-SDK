public class PDThemeColors {
    private String primaryAppColor;
    private String primaryInverseColor;
    private String viewBackgroundColor;
    private String primaryFontColor;
    private String secondaryFontColor;
    private String tableViewCellBackgroundColor;
    private String tableViewSeperatorColor;
    private String navBarTintColor;
    private String segmentedControlBackgroundColor;

    public  PDThemeColors() {}

    public String getPrimaryAppColor() {
        return primaryAppColor;
    }

    public void setPrimaryAppColor(String primaryAppColor) {
        this.primaryAppColor = primaryAppColor;
    }

    public String getPrimaryInverseColor() {
        return primaryInverseColor;
    }

    public void setPrimaryInverseColor(String primaryInverseColor) {
        this.primaryInverseColor = primaryInverseColor;
    }

    public String getViewBackgroundColor() {
        return viewBackgroundColor;
    }

    public void setViewBackgroundColor(String viewBackgroundColor) {
        this.viewBackgroundColor = viewBackgroundColor;
    }

    public String getPrimaryFontColor() {
        return primaryFontColor;
    }

    public void setPrimaryFontColor(String primaryFontColor) {
        this.primaryFontColor = primaryFontColor;
    }

    public String getSecondaryFontColor() {
        return secondaryFontColor;
    }

    public void setSecondaryFontColor(String secondaryFontColor) {
        this.secondaryFontColor = secondaryFontColor;
    }

    public String getTableViewCellBackgroundColor() {
        return tableViewCellBackgroundColor;
    }

    public void setTableViewCellBackgroundColor(String tableViewCellBackgroundColor) {
        this.tableViewCellBackgroundColor = tableViewCellBackgroundColor;
    }

    public String getTableViewSeperatorColor() {
        return tableViewSeperatorColor;
    }

    public void setTableViewSeperatorColor(String tableViewSeperatorColor) {
        this.tableViewSeperatorColor = tableViewSeperatorColor;
    }

    public String getNavBarTintColor() {
        return navBarTintColor;
    }

    public void setNavBarTintColor(String navBarTintColor) {
        this.navBarTintColor = navBarTintColor;
    }

    public String getSegmentedControlBackgroundColor() {
        return segmentedControlBackgroundColor;
    }

    public void setSegmentedControlBackgroundColor(String segmentedControlBackgroundColor) {
        this.segmentedControlBackgroundColor = segmentedControlBackgroundColor;
    }
}
