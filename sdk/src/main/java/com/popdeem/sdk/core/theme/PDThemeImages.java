public class PDThemeImages {
    private String loginImage;
    private String defaultItemImage;
    private String homeHeaderImage;

    public PDThemeImages() {}

    public String getLoginImage() {
        return loginImage;
    }

    public void setLoginImage(String loginImage) {
        this.loginImage = loginImage;
    }

    public String getDefaultItemImage() {
        return defaultItemImage;
    }

    public void setDefaultItemImage(String defaultItemImage) {
        this.defaultItemImage = defaultItemImage;
    }

    public String getHomeHeaderImage() {
        return homeHeaderImage;
    }

    public void setHomeHeaderImage(String homeHeaderImage) {
        this.homeHeaderImage = homeHeaderImage;
    }
}
